<script>
import { defineComponent, reactive, ref, toRefs } from "vue";
import axios from "axios";
import { message } from "ant-design-vue";
import { useRouter } from "vue-router";
import { useRoute } from "vue-router";

export default defineComponent({
  setup() {
    const router = useRouter();
    const route = useRoute();
    const errors = ref({});
    const validateloginErrorEmail = ref("");
    const validateloginErrorPassword = ref("");
    const validateloginErrorUsername = ref("");
    const errorEmail = ref("none");
    const errorPassword = ref("none");
    const errorName = ref("none");

    const register = reactive({
      name: "",
      email: "",
      password: "",
    });

    const createRegister = () => {
      if (validatelogin()) {
        axios
          .post("http://127.0.0.1:8000/api/register", register)
          .then((response) => {
            if (response) {
              message.success("Đăng kí tài khoản thành công!!");
              router.push({ name: "frontend-login" });
            }
          });
      }
    };

    const updateerror = () => {
      if (register.email.trim() !== "") {
        errorEmail.value = "none";
      }
    };

    const updateerrorPassword = () => {
      if (register.password.trim() !== "") {
        errorPassword.value = "none";
      }
    };

    const updateerrorName = () => {
      if (register.name.trim() !== "") {
        errorName.value = "none";
      }
    };
    const validatelogin = () => {
      if (register.name.trim() == "") {
        validateloginErrorUsername.value = "Bạn cần nhập username !!";
        errorName.value = "block";
        return false;
      }
      if (register.email.trim() === "") {
        validateloginErrorEmail.value = "Bạn cần nhập email !!";
        errorEmail.value = "block";
        return false;
      }

      //check email
      axios
        .get('http://127.0.0.1:8000/api/checkEmail', {
          params: {
            email: register.email
          }
        })
        .then((response) => {
          console.log(response.data.exists);
          if (response.data.exists==true) {
            validateloginErrorEmail.value="Email đã tồn tại";
            errorEmail.value = "block";
            return false
          }
        })
        .catch((error) => {
          console.error(error);
          // Xử lý lỗi nếu cần thiết
        });



      if (register.password.trim() === "") {
        validateloginErrorPassword.value = "Bạn cần nhập password !!";
        errorPassword.value = "block";
        return false;
      }
      validateloginErrorUsername.value = "";
      validateloginErrorEmail.value = "";
      validateloginErrorPassword.value = "";
      errorEmail.value = "none";
      return true;
    };

    return {
      errors,
      ...toRefs(register),
      createRegister,
      validateloginErrorUsername,
      validateloginErrorEmail,
      validateloginErrorPassword,
      errorEmail,
      errorPassword,
      errorName,
      updateerror,
      updateerrorPassword,
      updateerrorName,
    };
  },
});
</script>
